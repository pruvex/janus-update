function generateRunnerFunction(plan) {
  const win = plan.chatWindow || 'A';
  return `
async function runPromptInChatWindow(page, prompt) {
  const diagnostics = createPromptDiagnostics(page);
  const input = chatInput(page, '${win}');
  let streamRequestCount = 0;
  let streamRequestUrl = null;

  const onStreamRequest = (request) => {
    const url = request.url();
    if (request.method() === 'POST' && /\\/api\\/chat\\/stream\\/?(\\?|$)/.test(new URL(url).pathname)) {
      streamRequestCount++;
      streamRequestUrl = url;
    }
  };
  page.on('request', onStreamRequest);

  try {
    // Focus window and ensure input visible
    await chatWindow(page, '${win}').click();
    await expect(input).toBeVisible({ timeout: 5000 });

    // SEND STRATEGY: ${plan.strategies.send}
${plan.strategies.send === 'chat_button_click_send_v1' ? `    // Promise.all pattern: register stream listener BEFORE the click to eliminate
    // the race window where a fast SSE response could resolve before waitForResponse() attaches.
    // pressSequentially simulates real typing so the frontend's per-keystroke validation
    // (text present + model selected) flips the send button into the enabled state.
    await input.click();
    await input.pressSequentially(prompt, { delay: 30 });

    // DIAGNOSTIC: verify the textarea actually holds the prompt and the send button is enabled.
    // If either fails, the frontend submit handler will bail out at \`if (!promptText) return;\`
    // and no /api/chat/stream request can ever fire.
    const sendBtnLoc = page.locator('#send-button-${win}');
    const preClickDiag = await page.evaluate((w) => {
      const ta = document.getElementById('user-input-' + w);
      const btn = document.getElementById('send-button-' + w);
      const form = document.getElementById('chat-form-' + w);
      const taRect = ta?.getBoundingClientRect?.();
      const btnRect = btn?.getBoundingClientRect?.();
      return {
        textareaFound: !!ta,
        textareaValue: ta?.value ?? null,
        textareaValueLen: (ta?.value ?? '').length,
        textareaTrimmedLen: ((ta?.value ?? '').trim()).length,
        textareaVisible: !!(taRect && taRect.width > 0 && taRect.height > 0),
        buttonFound: !!btn,
        buttonDisabled: btn?.disabled ?? null,
        buttonAriaDisabled: btn?.getAttribute?.('aria-disabled') ?? null,
        buttonType: btn?.type ?? null,
        buttonVisible: !!(btnRect && btnRect.width > 0 && btnRect.height > 0),
        formFound: !!form,
        buttonInForm: !!(btn && form && form.contains(btn)),
        activeElementId: document.activeElement?.id ?? null,
        bodyHasOverlay: !!document.querySelector('.modal-overlay:not([hidden]), .modal.show, [aria-modal="true"]'),
        overlayDetails: Array.from(document.querySelectorAll('.modal-overlay:not([hidden]), .modal.show, [aria-modal="true"]')).map((el) => ({
          tag: el.tagName,
          id: el.id || null,
          classes: el.className || null,
          ariaModal: el.getAttribute('aria-modal'),
          hidden: el.hasAttribute('hidden'),
          rect: (() => { const r = el.getBoundingClientRect(); return { x: r.x, y: r.y, w: r.width, h: r.height }; })(),
          zIndex: getComputedStyle(el).zIndex,
          pointerEvents: getComputedStyle(el).pointerEvents,
          displayed: getComputedStyle(el).display,
        })),
        sendBtnTopElement: (() => {
          const btn = document.getElementById('send-button-' + w);
          if (!btn) return null;
          const r = btn.getBoundingClientRect();
          const cx = r.left + r.width / 2;
          const cy = r.top + r.height / 2;
          const top = document.elementFromPoint(cx, cy);
          if (!top) return null;
          return { tag: top.tagName, id: top.id || null, classes: top.className || null, isBtn: top === btn };
        })(),
      };
    }, '${win}');
    console.log('[E2E-DIAG] preClick:', JSON.stringify(preClickDiag));
    if (preClickDiag.textareaTrimmedLen === 0) {
      throw new Error('RUNNER_PRECLICK_EMPTY: textarea #user-input-${win} is empty after pressSequentially. ' + JSON.stringify(preClickDiag));
    }
    if (!preClickDiag.buttonInForm) {
      throw new Error('RUNNER_PRECLICK_DOM_BROKEN: #send-button-${win} is not nested inside #chat-form-${win}. ' + JSON.stringify(preClickDiag));
    }

    // CRITICAL FIX: dock-bar (position: fixed; bottom: 0; z-index: 90) geometrically overlaps
    // the send button in the test viewport. Coordinate-based click({force:true}) lands on
    // #dock-bar and never reaches #send-button-${win}. Use DOM-level HTMLElement.click() via
    // evaluate(), which dispatches the click event directly on the target element and
    // triggers native form submission regardless of what sits on top geometrically.
    const [streamResponse] = await Promise.all([
      page.waitForResponse(
        (res) => res.url().includes('/api/chat/stream') && res.status() === 200,
        { timeout: STREAM_REQUEST_TIMEOUT_MS },
      ),
      sendBtnLoc.evaluate((btn) => btn.click()),
    ]);
    void streamResponse;` : plan.strategies.send === 'form_request_submit_v1' ? `    await input.fill(prompt);
    // Direct form submit via requestSubmit (bypasses overlay issues)
    await page.locator('#chat-form-${win}').evaluate((form) => form.requestSubmit());
    await page.waitForTimeout(300);` : `    await input.fill(prompt);
    // Submit via real UI interaction (Enter key)
    await input.press('Enter');
    await page.waitForTimeout(300);`}

    // Patient polling: wait until the assistant bubble holds real content,
    // not just the "..." loading placeholder (which may carry an appended timestamp).
    try {
      await expect(async () => {
        const bubble = page.locator('#chat-messages-${win} .message.assistant').last();
        await expect(bubble).toBeVisible();
        const text = await bubble.innerText();
        const trimmed = text.trim();
        if (trimmed.startsWith('...')) throw new Error('Still loading');
        expect(trimmed.length).toBeGreaterThan(15);
      }).toPass({ timeout: ASSISTANT_RESPONSE_TIMEOUT_MS });
    } catch (_) {
      throw new Error('RUNNER_STREAM_TIMEOUT: Assistant bubble appeared but is empty or only contains "..." with timestamp. SSE stream observed but no real content rendered.');
    }

    const assistantMessage = assistantResponse(page, '${win}');

    // Verify stream request was actually observed (bubble without stream = frontend bug)
    if (streamRequestCount === 0) {
      throw new Error('RUNNER_STREAM_TIMEOUT: Assistant bubble appeared but no POST /api/chat/stream request was observed. Frontend may have rendered static content without backend call.');
    }

    // Check for visible Janus errors
    const errorMessage = page.locator(\`#chat-messages-${win} .message.error, #chat-messages-${win} .message.system\`).last();
    const hasError = await errorMessage.count() > 0;
    if (hasError) {
      const errorText = await errorMessage.textContent();
      throw new Error(\`PROVIDER_TIMEOUT: Visible Janus error bubble: \${errorText}\`);
    }

    const responseText = await assistantMessage.textContent();
    return responseText || '';
  } catch (error) {
    const apiEvents = diagnostics.events.filter((event) => event.url);
    const chatEvents = apiEvents.filter((event) => /\\/api\\/.*(chat|message)/i.test(event.url));
    const streamEvents = apiEvents.filter((event) => /\\/api\\/chat\\/stream/i.test(event.url));
    const failedEvents = diagnostics.events.filter(
      (event) => event.type === 'requestfailed' || event.type === 'pageerror' || event.type.startsWith('console:')
    );
    const sendMessageLogs = diagnostics.consoleLogs.filter((log) => log.text.includes('[sendMessage]'));
    const sseLogs = diagnostics.consoleLogs.filter((log) => log.text.includes('[SSE'));

    // DOM diagnostics: capture chat-messages state even when page is still open
    let domDump = 'N/A';
    let messageTexts = 'N/A';
    let domEval = 'N/A';
    try {
      const chatMessagesEl = page.locator(\`#chat-messages-\${win}\`).first();
      domDump = await chatMessagesEl.innerHTML({ timeout: 2000 });
      const msgs = await page.locator(\`#chat-messages-\${win} .message\`).all();
      messageTexts = await Promise.all(msgs.map(async (m) => await m.textContent()));
    } catch (e) { messageTexts = 'ERR: ' + String(e?.message || e).slice(0, 200); }
    try {
      domEval = await page.evaluate((w) => {
        const cm = document.getElementById('chat-messages-' + w);
        if (!cm) return { found: false };
        const messages = Array.from(cm.querySelectorAll('.message')).map((m, i) => ({
          i,
          className: m.className,
          bubbleHTMLLen: m.querySelector('.bubble')?.innerHTML?.length ?? 0,
          bubbleTextLen: m.querySelector('.bubble')?.textContent?.length ?? 0,
          bubbleTextSample: (m.querySelector('.bubble')?.textContent ?? '').slice(0, 80),
          isConnected: m.isConnected,
        }));
        return {
          found: true,
          containerChildCount: cm.childElementCount,
          messageCount: messages.length,
          messages,
          containerHTMLLen: cm.innerHTML.length,
          containerHTMLSample: cm.innerHTML.slice(0, 400),
        };
      }, '${win}');
    } catch (e) { domEval = 'EVAL_ERR: ' + String(e?.message || e).slice(0, 200); }

    if (page.isClosed()) {
      throw new Error(
        \`RUNNER_WAIT_FAILURE: Playwright page closed before completion for prompt "\${prompt}".\\nChat Events: \${JSON.stringify(chatEvents)}\\nStream Events: \${JSON.stringify(streamEvents)}\\nFailures: \${JSON.stringify(failedEvents)}\\nOriginal: \${error.message}\`
      );
    }

    // Re-throw if already classified — but enrich with SSE diagnostics so we can
    // see where the frontend stream-render pipeline actually broke.
    if (/^(RUNNER_|FRONTEND_|BACKEND_|PROVIDER_|TOOL_|ASSERTION_)/.test(error.message)) {
      throw new Error(\`\${error.message}\nConsole [SSE]: \${JSON.stringify(sseLogs)}\nDOM eval: \${JSON.stringify(domEval)}\nDOM message texts: \${JSON.stringify(messageTexts)}\`);
    }

    // Distinguish backend/provider errors from runner errors based on stream observation
    if (streamEvents.length === 0) {
      throw new Error(
        \`RUNNER_STREAM_TIMEOUT: No POST /api/chat/stream observed after "\${prompt}".\nChat Events: \${JSON.stringify(chatEvents)}\nConsole [sendMessage]: \${JSON.stringify(sendMessageLogs)}\nConsole [SSE]: \${JSON.stringify(sseLogs)}\nDiagnostics: \${JSON.stringify(diagnostics.events)}\nDOM messages: \${JSON.stringify(messageTexts)}\nOriginal: \${error.message}\`
      );
    }

    throw new Error(
      \`PROVIDER_TIMEOUT: Assistant response not received within \${ASSISTANT_RESPONSE_TIMEOUT_MS}ms after "\${prompt}". Stream request was observed but no assistant message rendered.\nChat Events: \${JSON.stringify(chatEvents)}\nStream Events: \${JSON.stringify(streamEvents)}\nFailures: \${JSON.stringify(failedEvents)}\nConsole [SSE]: \${JSON.stringify(sseLogs)}\nDOM dump: \${domDump.slice(0, 1200)}\nDOM message texts: \${JSON.stringify(messageTexts)}\nOriginal: \${error.message}\`
    );
  } finally {
    page.off('request', onStreamRequest);
    diagnostics.dispose();
  }
}
`;
}

function generateEvaluator(testCase) {
  const exp = testCase.expected || {};
  const checks = [];

  if (Array.isArray(exp.containsAny) && exp.containsAny.length > 0) {
    const terms = exp.containsAny.map((t) => `'${t.replace(/'/g, "\\'")}'`).join(', ');
    checks.push(`    const hasContainsAny = [${terms}].some((term) => responseText.toLowerCase().includes(term.toLowerCase()));`);
  } else {
    checks.push(`    const hasContainsAny = true; // no containsAny requirement`);
  }

  if (Array.isArray(exp.mustNotContain) && exp.mustNotContain.length > 0) {
    const terms = exp.mustNotContain.map((t) => `'${t.replace(/'/g, "\\'")}'`).join(', ');
    checks.push(`    const hasMustNotContain = [${terms}].some((term) => responseText.toLowerCase().includes(term.toLowerCase()));`);
  } else {
    checks.push(`    const hasMustNotContain = false; // no mustNotContain requirement`);
  }

  checks.push(`    const passed = hasContainsAny && !hasMustNotContain;`);
  return checks.join('\n');
}

function generateTestCase(testCase, plan) {
  const exp = testCase.expected || {};
  const hasEval = (Array.isArray(exp.containsAny) && exp.containsAny.length > 0) ||
                  (Array.isArray(exp.mustNotContain) && exp.mustNotContain.length > 0);

  if (testCase.type === 'manual_gate') {
    return `
  test('${testCase.id}: ${testCase.name.replace(/'/g, "\\'")}', async () => {
    test.info().annotations.push({
      type: 'manual_gate_required',
      description: '${(testCase.prompt || 'Manual action required').replace(/'/g, "\\'")}'
    });
    writeEvidence('${testCase.id}', 'MANUAL_GATE_REQUIRED', 'MANUAL_GATE_REQUIRED', buildEvidence('', '', 0), 'User must manually execute: ${(testCase.prompt || '').replace(/'/g, "\\'")}');
  });`;
  }

  const body = [];
  body.push(`    test.setTimeout(TEST_CASE_TIMEOUT_MS);`);
  body.push(`    const prompt = '${testCase.prompt.replace(/'/g, "\\'")}';`);

  if (exp.responseTimeMsMax) {
    body.push(`    const startTime = Date.now();`);
  }

  body.push(`    const responseText = await runPromptInChatWindow(page, prompt);`);

  if (exp.responseTimeMsMax) {
    body.push(`    const responseTime = Date.now() - startTime;`);
  }

  if (hasEval) {
    body.push(generateEvaluator(testCase));
    body.push(`    writeEvidence('${testCase.id}', passed ? 'PASS' : 'FAIL', passed ? 'ASSERTION_MISMATCH' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0), passed ? 'Expectations met' : 'Expectations not met');`);
    body.push(`    expect(passed, \`Test ${testCase.id} failed: containsAny/mustNotContain not satisfied. Response: \${responseText.slice(0, 200)}\`).toBe(true);`);
  } else if (exp.responseTimeMsMax) {
    body.push(`    const withinTime = responseTime < ${exp.responseTimeMsMax};`);
    body.push(`    writeEvidence('${testCase.id}', withinTime ? 'PASS' : 'FAIL', withinTime ? 'ASSERTION_MISMATCH' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, responseTime), withinTime ? \`Response in \${responseTime}ms\` : \`Too slow: \${responseTime}ms\`);`);
    body.push(`    expect(withinTime, \`Test ${testCase.id} exceeded response time: \${responseTime}ms > ${exp.responseTimeMsMax}ms\`).toBe(true);`);
  } else if (exp.requiresConfirmation) {
    body.push(`    const hasConfirmation = /bist du sicher|bestÃƒÂ¤tigen|wirklich/i.test(responseText);`);
    body.push(`    writeEvidence('${testCase.id}', hasConfirmation ? 'PASS' : 'FAIL', hasConfirmation ? 'ASSERTION_MISMATCH' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0), hasConfirmation ? 'Confirmation requested' : 'No confirmation detected');`);
    body.push(`    expect(hasConfirmation, \`Test ${testCase.id}: expected confirmation prompt not found. Response: \${responseText.slice(0, 200)}\`).toBe(true);`);
  } else {
    body.push(`    const hasResponse = responseText && responseText.length > 20;`);
    body.push(`    writeEvidence('${testCase.id}', hasResponse ? 'PASS' : 'FAIL', hasResponse ? 'ASSERTION_MISMATCH' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0), hasResponse ? 'Assistant responded' : 'No meaningful response');`);
    body.push(`    expect(hasResponse, \`Test ${testCase.id}: no meaningful assistant response.\`).toBe(true);`);
  }

  return `
  test('${testCase.id}: ${testCase.name.replace(/'/g, "\\'")}', async ({ page }) => {
${body.join('\n')}
  });`;
}

function generateSuite(plan) {
  const groups = {};
  for (const t of plan.tests) {
    const g = t.type || 'functional';
    if (!groups[g]) groups[g] = [];
    groups[g].push(t);
  }

  const groupOrder = ['functional', 'intent_routing', 'ux', 'security', 'prompt_injection', 'cost_token', 'manual_gate'];
  const orderedGroups = [];
  for (const g of groupOrder) {
    if (groups[g]) orderedGroups.push({ name: g, tests: groups[g] });
  }
  // Any remaining groups not in the standard order
  for (const g of Object.keys(groups)) {
    if (!groupOrder.includes(g)) orderedGroups.push({ name: g, tests: groups[g] });
  }

  const sections = [];
  for (const { name, tests } of orderedGroups) {
    const label = name.replace(/_/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase()) + ' Tests';
    const cases = tests.map((t) => generateTestCase(t, plan)).join('\n');
    sections.push(`  test.describe('${label}', () => {\n${cases}\n  });`);
  }

  return sections.join('\n\n');
}

function generateBeforeEach(plan) {
  const win = plan.chatWindow || 'A';
  return `
  test.beforeEach(async ({ page }) => {
    const { config } = loadJanusAppDataConfig();
    await installInternalApiKeyRoute(page, config.api_key);

    await page.goto('${plan.baseUrl}');
    const token = createE2eJwt();
    await page.evaluate(() => localStorage.clear());
    await page.evaluate((jwt) => localStorage.setItem('auth_token', jwt), token);
    await page.reload();

    await page.waitForFunction(async () => {
      const jwt = localStorage.getItem('auth_token');
      if (!jwt) return false;
      const response = await fetch('http://127.0.0.1:8001/api/users/me', {
        headers: { Authorization: \`Bearer \${jwt}\` },
      });
      return response.ok;
    }, null, { timeout: 15000 });

    await expect(page.getByRole('button', { name: 'Neuer Chat' })).toBeVisible({ timeout: 15000 });
    await expect(chatInput(page, '${win}')).toBeVisible({ timeout: 15000 });

    // Create clean chat in window ${win}
    await chatWindow(page, '${win}').click();
    const chatsPost = page.waitForResponse(
      (res) =>
        res.request().method() === 'POST' &&
        /\\/api\\/chats\\/?(\\?|$)/.test(new URL(res.url()).pathname) &&
        res.ok(),
      { timeout: 20000 },
    );
    await page.getByRole('button', { name: 'Neuer Chat' }).first().click();
    await chatsPost;
    await expect(page.locator('#chat-messages-${win} .message')).toHaveCount(0, { timeout: 5000 });
  });
`;
}

function generateDescribe(plan) {
  return `test.describe('${plan.testRunId}: ${plan.title}', () => {
  test.describe.configure({ mode: 'serial' });
${generateBeforeEach(plan)}
${generateSuite(plan)}
});`;
}

