function generateHelpers() {
  return `
function base64Url(input) {
  return Buffer.from(input)
    .toString('base64')
    .replace(/=/g, '')
    .replace(/\\+/g, '-')
    .replace(/\\//g, '_');
}

function loadJanusAppDataConfig() {
  const appData = process.env.APPDATA || '';
  const configPath = path.join(appData, 'Janus Projekt', 'config.json');
  return { configPath, config: JSON.parse(fs.readFileSync(configPath, 'utf-8')) };
}

function createE2eJwt() {
  const { configPath, config } = loadJanusAppDataConfig();
  const secret = config.jwt_secret_key;
  if (!secret) throw new Error(\`jwt_secret_key missing in \${configPath}\`);

  const header = { alg: 'HS256', typ: 'JWT' };
  const payload = {
    sub: 'local_user',
    scopes: ['me', 'settings:write'],
    exp: Math.floor(Date.now() / 1000) + 60 * 60,
  };
  const unsigned = \`\${base64Url(JSON.stringify(header))}.\${base64Url(JSON.stringify(payload))}\`;
  const signature = crypto
    .createHmac('sha256', secret)
    .update(unsigned)
    .digest('base64')
    .replace(/=/g, '')
    .replace(/\\+/g, '-')
    .replace(/\\//g, '_');
  return \`\${unsigned}.\${signature}\`;
}

async function installInternalApiKeyRoute(page, internalKey) {
  if (!internalKey) {
    throw new Error('api_key missing in Janus config.json (required for E2E against real backend)');
  }
  const handler = async (route) => {
    const headers = { ...route.request().headers() };
    headers['X-Janus-Internal-Key'] = internalKey;
    await route.continue({ headers });
  };
  await page.route('http://127.0.0.1:8001/api/**', handler);
  await page.route('http://localhost:8001/api/**', handler);
}

function chatWindow(page, windowName) {
  return page.getByRole('region', { name: \`Chat-Fenster \${windowName}\` });
}

function chatInput(page, windowName) {
  return chatWindow(page, windowName).getByPlaceholder(/Nachricht an Janus senden/);
}

function assistantResponse(page, windowName) {
  return page.locator(\`#chat-messages-\${windowName} .message.assistant\`).last();
}

function compactApiUrl(url) {
  try {
    const parsed = new URL(url);
    return \`\${parsed.origin}\${parsed.pathname}\`;
  } catch {
    return url;
  }
}
`;
}

function generateDiagnostics() {
  return `
function createPromptDiagnostics(page) {
  const events = [];
  const consoleLogs = [];
  const pushEvent = (event) => {
    events.push({ timestamp: new Date().toISOString(), ...event });
    if (events.length > 200) events.shift();
  };
  const pushConsoleLog = (log) => {
    consoleLogs.push({ timestamp: new Date().toISOString(), ...log });
    if (consoleLogs.length > 400) consoleLogs.shift();
  };

  const onRequest = (request) => {
    const url = request.url();
    if (!url.includes('/api/')) return;
    pushEvent({ type: 'request', method: request.method(), url: compactApiUrl(url) });
  };
  const onResponse = (response) => {
    const url = response.url();
    if (!url.includes('/api/')) return;
    pushEvent({ type: 'response', status: response.status(), url: compactApiUrl(url) });
  };
  const onRequestFailed = (request) => {
    const url = request.url();
    if (!url.includes('/api/')) return;
    pushEvent({
      type: 'requestfailed',
      method: request.method(),
      url: compactApiUrl(url),
      failure: request.failure()?.errorText || 'unknown',
    });
  };
  const onConsole = (message) => {
    const text = message.text();
    pushConsoleLog({ type: message.type(), text: text.slice(0, 1000) });
    if (!['error', 'warning'].includes(message.type())) return;
    pushEvent({ type: \`console:\${message.type()}\`, text: text.slice(0, 500) });
  };
  const onPageError = (error) => {
    pushEvent({ type: 'pageerror', text: String(error?.message || error).slice(0, 500) });
  };

  page.on('request', onRequest);
  page.on('response', onResponse);
  page.on('requestfailed', onRequestFailed);
  page.on('console', onConsole);
  page.on('pageerror', onPageError);

  return {
    events,
    consoleLogs,
    dispose() {
      page.off('request', onRequest);
      page.off('response', onResponse);
      page.off('requestfailed', onRequestFailed);
      page.off('console', onConsole);
      page.off('pageerror', onPageError);
    },
  };
}
`;
}

