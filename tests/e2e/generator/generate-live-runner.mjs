#!/usr/bin/env node
/**
 * Deterministic Playwright Live Runner Generator for Janus Test Pipeline.
 *
 * Usage:
 *   node tests/e2e/generator/generate-live-runner.mjs \
 *     --plan documentation/test-runs/TEST-RUN-2026-05-11-005_plan.json \
 *     --out tests/e2e/generated/TEST-RUN-2026-05-11-005.live.spec.js
 *
 * Rules:
 * - Same TestPlan -> same Runner (no LLM, no randomness, no timestamps in output).
 * - Unknown strategy -> TESTPLAN INVALID.
 * - Missing required fields -> TESTPLAN INVALID.
 * - Generated code uses existing Janus E2E helpers and real UI interactions only.
 */

