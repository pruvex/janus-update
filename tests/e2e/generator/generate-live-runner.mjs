function generateEvaluator(testCase) {
  const exp = testCase.expected || {};
  const checks = [];

  if (Array.isArray(exp.containsAny) && exp.containsAny.length > 0) {
    const terms = exp.containsAny.map((t) => `'${t.replace(/'/g, "\\'")}'`).join(', ');
    checks.push(`    const hasContainsAny = [${terms}].some((term) => responseText.toLowerCase().includes(term.toLowerCase()));`);
  } else {
    checks.push(`    const hasContainsAny = true; // no containsAny requirement`);
  }

  if (Array.isArray(exp.mustNotContain) && exp.mustNotContain.length > 0) {
    const terms = exp.mustNotContain.map((t) => `'${t.replace(/'/g, "\\'")}'`).join(', ');
    checks.push(`    const hasMustNotContain = [${terms}].some((term) => responseText.toLowerCase().includes(term.toLowerCase()));`);
  } else {
    checks.push(`    const hasMustNotContain = false; // no mustNotContain requirement`);
  }

  checks.push(`    const passed = hasContainsAny && !hasMustNotContain;`);
  return checks.join('\n');
}

function generateTestCase(testCase, plan) {
  const exp = testCase.expected || {};
  const hasEval = (Array.isArray(exp.containsAny) && exp.containsAny.length > 0) ||
                  (Array.isArray(exp.mustNotContain) && exp.mustNotContain.length > 0);
  // BACKLOG-029 (ATOMIC SCOPE FIX): resolve the chat-window id at generator time so
  // the emitted runPromptInChatWindow call passes a hardcoded string (e.g. 'A')
  // rather than relying on any closure variable in the generated spec scope.
  const callWin = plan.chatWindow || 'A';

  if (testCase.type === 'manual_gate') {
    return `
  test('${testCase.id}: ${testCase.name.replace(/'/g, "\\'")}', async () => {
    test.info().annotations.push({
      type: 'manual_gate_required',
      description: '${(testCase.prompt || 'Manual action required').replace(/'/g, "\\'")}'
    });
    writeEvidence('${testCase.id}', 'MANUAL_GATE_REQUIRED', 'MANUAL_GATE_REQUIRED', buildEvidence('', '', 0), 'User must manually execute: ${(testCase.prompt || '').replace(/'/g, "\\'")}');
  });`;
  }

  // BACKLOG-028: Tool-Routing-Verification. Accept both \`expected.tool\` (new) and
  // \`expected.toolCallExpected\` (existing Skill-1 schema convention) so legacy
  // plans (e.g. TEST-RUN-2026-05-12-001) keep working.
  const expectedTool = exp.tool || exp.toolCallExpected;
  const expectedToolEsc = expectedTool ? expectedTool.replace(/'/g, "\\'") : '';

  const body = [];
  body.push(`    test.setTimeout(TEST_CASE_TIMEOUT_MS);`);
  body.push(`    const prompt = '${testCase.prompt.replace(/'/g, "\\'")}';`);

  if (exp.responseTimeMsMax) {
    body.push(`    const startTime = Date.now();`);
  }

  body.push(`    const { responseText, triggeredTools } = await runPromptInChatWindow(page, prompt, '${callWin}');`);

  if (exp.responseTimeMsMax) {
    body.push(`    const responseTime = Date.now() - startTime;`);
  }

  if (expectedTool) {
    // Normalize "." <-> "_" because OpenAI's tool-name normalization maps
    // \`system.weather\` -> \`system_weather\` (see execution_engine.py).
    body.push(`    const expectedTool = '${expectedToolEsc}';`);
    body.push(`    const normalizeToolName = (s) => String(s || '').replace(/\\./g, '_').toLowerCase();`);
    body.push(`    const expectedToolNorm = normalizeToolName(expectedTool);`);
    body.push(`    const toolMatch = triggeredTools.some((t) => normalizeToolName(t) === expectedToolNorm);`);
    body.push(`    if (!toolMatch) {`);
    body.push(`      writeEvidence('${testCase.id}', 'FAIL', 'TOOL_ROUTING_FAILURE', buildEvidence(prompt, responseText, ${exp.responseTimeMsMax ? 'responseTime' : '0'}, triggeredTools), \`Expected tool "\${expectedTool}" was not triggered. Tools called: \${triggeredTools.join(', ') || 'none'}\`);`);
    body.push(`      throw new Error(\`TOOL_ROUTING_FAILURE: Expected tool "\${expectedTool}" was not triggered. Tools called: \${triggeredTools.join(', ') || 'none'}\`);`);
    body.push(`    }`);
  }

  if (hasEval) {
    body.push(generateEvaluator(testCase));
    body.push(`    writeEvidence('${testCase.id}', passed ? 'PASS' : 'FAIL', passed ? 'ASSERTION_PASS' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0, triggeredTools), passed ? 'Expectations met' : 'Expectations not met');`);
    body.push(`    expect(passed, \`Test ${testCase.id} failed: containsAny/mustNotContain not satisfied. Response: \${responseText.slice(0, 200)}\`).toBe(true);`);
  } else if (exp.responseTimeMsMax) {
    body.push(`    const withinTime = responseTime < ${exp.responseTimeMsMax};`);
    body.push(`    writeEvidence('${testCase.id}', withinTime ? 'PASS' : 'FAIL', withinTime ? 'RESPONSE_TIME_PASS' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, responseTime, triggeredTools), withinTime ? \`Response in \${responseTime}ms\` : \`Too slow: \${responseTime}ms\`);`);
    body.push(`    expect(withinTime, \`Test ${testCase.id} exceeded response time: \${responseTime}ms > ${exp.responseTimeMsMax}ms\`).toBe(true);`);
  } else if (exp.requiresConfirmation) {
    body.push(`    const hasConfirmation = /bist du sicher|bestÃƒÂ¤tigen|wirklich/i.test(responseText);`);
    body.push(`    writeEvidence('${testCase.id}', hasConfirmation ? 'PASS' : 'FAIL', hasConfirmation ? 'CONFIRMATION_PASS' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0, triggeredTools), hasConfirmation ? 'Confirmation requested' : 'No confirmation detected');`);
    body.push(`    expect(hasConfirmation, \`Test ${testCase.id}: expected confirmation prompt not found. Response: \${responseText.slice(0, 200)}\`).toBe(true);`);
  } else if (expectedTool) {
    // \`expected.tool\` was the only check requested -> emit PASS evidence with
    // the recorded tool list (the throw above already covered the FAIL path).
    body.push(`    writeEvidence('${testCase.id}', 'PASS', 'TOOL_ROUTING_PASS', buildEvidence(prompt, responseText, 0, triggeredTools), \`Expected tool "\${expectedTool}" was triggered (observed: \${triggeredTools.join(', ')})\`);`);
  } else {
    body.push(`    const hasResponse = responseText && responseText.length > 20;`);
    body.push(`    writeEvidence('${testCase.id}', hasResponse ? 'PASS' : 'FAIL', hasResponse ? 'RESPONSE_PRESENT_PASS' : 'ASSERTION_MISMATCH', buildEvidence(prompt, responseText, 0, triggeredTools), hasResponse ? 'Assistant responded' : 'No meaningful response');`);
    body.push(`    expect(hasResponse, \`Test ${testCase.id}: no meaningful assistant response.\`).toBe(true);`);
  }

  return `
  test('${testCase.id}: ${testCase.name.replace(/'/g, "\\'")}', async ({ page }) => {
${body.join('\n')}
  });`;
}

function generateSuite(plan) {
  const groups = {};
  for (const t of plan.tests) {
    const safety = t.parallelSafe ? 'parallel' : 'serial';
    const g = `${safety}:${t.type || 'functional'}`;
    if (!groups[g]) groups[g] = [];
    groups[g].push(t);
  }

  const groupOrder = ['functional', 'intent_routing', 'ux', 'security', 'prompt_injection', 'cost_token', 'manual_gate'];
  const orderedGroups = [];
  for (const safety of ['parallel', 'serial']) {
    for (const g of groupOrder) {
      const key = `${safety}:${g}`;
      if (groups[key]) orderedGroups.push({ key, safety, name: g, tests: groups[key] });
    }
  }
  // Any remaining groups not in the standard order
  for (const g of Object.keys(groups)) {
    const [, name = g] = g.split(':');
    if (!orderedGroups.some((entry) => entry.key === g)) {
      orderedGroups.push({ key: g, safety: g.startsWith('parallel:') ? 'parallel' : 'serial', name, tests: groups[g] });
    }
  }

  const sections = [];
  for (const { name, safety, tests } of orderedGroups) {
    const label = `${name.replace(/_/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase())} Tests (${safety})`;
    const configure = safety === 'parallel' ? `\n    test.describe.configure({ mode: 'parallel' });` : '';
    const cases = tests.map((t) => generateTestCase(t, plan)).join('\n');
    sections.push(`  test.describe('${label}', () => {${configure}\n${cases}\n  });`);
  }

  return sections.join('\n\n');
}

