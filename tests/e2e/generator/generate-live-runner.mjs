function parseArgs(argv) {
  const args = {};
  for (let i = 2; i < argv.length; i += 2) {
    const key = argv[i];
    const val = argv[i + 1];
    if (key === '--plan') args.plan = val;
    if (key === '--out') args.out = val;
  }
  return args;
}

