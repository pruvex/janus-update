function main() {
  const args = parseArgs(process.argv);
  const planPath = args.plan;
  const outPath = args.out;

  if (!planPath || !outPath) {
    console.error('TESTPLAN INVALID: Usage: node generate-live-runner.mjs --plan <plan.json> --out <runner.spec.js>');
    process.exit(1);
  }

  if (!fs.existsSync(planPath)) {
    console.error(`TESTPLAN INVALID: Plan file not found: ${planPath}`);
    process.exit(1);
  }

  let plan;
  try {
    plan = JSON.parse(fs.readFileSync(planPath, 'utf-8'));
  } catch (err) {
    console.error(`TESTPLAN INVALID: JSON parse error: ${err.message}`);
    process.exit(1);
  }

  const registryPath = path.join(__dirname, 'strategy-registry.json');
  const registry = JSON.parse(fs.readFileSync(registryPath, 'utf-8'));

  const errors = validateTestPlan(plan, registry);
  if (errors.length > 0) {
    console.error('TESTPLAN INVALID:');
    for (const e of errors) console.error(`  - ${e}`);
    process.exit(1);
  }

  const parts = [
    generateHeader(plan),
    generateHelpers(),
    generateDiagnostics(),
    generateEvidenceWriter(plan),
    generateRunnerFunction(plan),
    generateDescribe(plan),
  ];

  const output = parts.join('\n') + '\n';

  const outDir = path.dirname(outPath);
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
  }
  fs.writeFileSync(outPath, output, 'utf-8');

  console.log(`GENERATED: ${outPath}`);
  console.log(`  TestRun: ${plan.testRunId}`);
  console.log(`  Tests:   ${plan.tests.length}`);
  console.log(`  Strategies: send=${plan.strategies.send}, wait=${plan.strategies.wait}, evidence=${plan.strategies.evidence}, evaluate=${plan.strategies.evaluate}`);
}

main();
