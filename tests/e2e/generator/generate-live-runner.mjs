function validateTestPlan(plan, registry) {
  const errors = [];

  const requiredTop = ['testRunId', 'title', 'executionMode', 'target', 'chatWindow', 'baseUrl', 'backendHealthUrl', 'timeouts', 'strategies', 'tests'];
  for (const f of requiredTop) {
    if (!(f in plan)) errors.push(`Missing required field: ${f}`);
  }

  if (!plan.testRunId || !/^TEST-RUN-\d{4}-\d{2}-\d{2}-\d{3}$/.test(plan.testRunId)) {
    errors.push(`Invalid testRunId format: ${plan.testRunId}`);
  }

  const validModes = ['LIVE_VISUAL', 'HEADLESS', 'LIVE_RETEST'];
  if (!validModes.includes(plan.executionMode)) {
    errors.push(`Invalid executionMode: ${plan.executionMode}`);
  }

  const validTargets = ['JANUS_CHAT', 'JANUS_DASHBOARD', 'JANUS_ELECTRON'];
  if (!validTargets.includes(plan.target)) {
    errors.push(`Invalid target: ${plan.target}`);
  }

  if (!plan.timeouts || typeof plan.timeouts.testCaseMs !== 'number') errors.push('Missing timeouts.testCaseMs');
  if (!plan.timeouts || typeof plan.timeouts.assistantResponseMs !== 'number') errors.push('Missing timeouts.assistantResponseMs');
  if (!plan.timeouts || typeof plan.timeouts.streamRequestMs !== 'number') errors.push('Missing timeouts.streamRequestMs');

  const strat = plan.strategies || {};
  const requiredStrategies = ['send', 'wait', 'evidence', 'evaluate'];
  for (const s of requiredStrategies) {
    if (!strat[s]) errors.push(`Missing strategy: strategies.${s}`);
    else {
      const available = Object.keys(registry.strategies[s] || {});
      if (!available.includes(strat[s])) {
        errors.push(`Unknown strategy "${strat[s]}" for type "${s}". Available: ${available.join(', ') || 'none'}`);
      }
    }
  }

  if (!Array.isArray(plan.tests) || plan.tests.length === 0) {
    errors.push('tests must be a non-empty array');
  } else {
    for (const t of plan.tests) {
      if (!t.id) errors.push('Test missing id');
      if (!t.name) errors.push(`Test ${t.id || '?'} missing name`);
      if (!t.prompt) errors.push(`Test ${t.id || '?'} missing prompt`);
      if (!t.type) errors.push(`Test ${t.id || '?'} missing type`);
      if (!t.provider) errors.push(`Test ${t.id || '?'} missing provider`);
      if (!t.model) errors.push(`Test ${t.id || '?'} missing model`);
      if (!t.expected) errors.push(`Test ${t.id || '?'} missing expected`);
    }
  }

  return errors;
}

